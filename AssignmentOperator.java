public class AssignmentOperator {
    public static void main(String[] args){
        System.out.println("\n\n\n");
        float x,y;
        x = 3;
        y = 2; // =
        x+=y; // x = x + y
        System.out.println("x : "+x);
        x-=y; // x = x - y
        System.out.println("x : "+x);
        x*=y; // x = x * y
        System.out.println("x : "+x);
        x/=y; // x = x / y
        System.out.println("x : "+x);
        x%=y; // x = x % y 
        System.out.println("x : "+x);
        System.out.println("\n\n\n");
    }
}
