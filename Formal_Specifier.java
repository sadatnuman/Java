public class Formal_Specifier {
    public static void main(String[] args){
        boolean B = true;
        char C = 'N';
        byte b = 10;
        short S = 11;
        int I = 12;
        long L = 13;
        float F = 14.12f;
        double D = 15.00;
        System.out.println("\n\n\n");
        System.out.printf("Boolean : %B\n",B);
        System.out.printf("Char : %c\n",C);
        System.out.printf("Byte : %d\n",b);
        System.out.printf("Short : %d\n",S);
        System.out.printf("Int : %d\n",I);
        System.out.printf("Long : %d\n",L);
        System.out.printf("Float : %f\n",F);                   
        //System.out.printf("Float : %.2f\n",F);
        System.out.printf("Double : %f\n",D);               
        //System.out.printf("Double : %.3f\n",D);
        System.out.println("\n\n\n");
    }
}
