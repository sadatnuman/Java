public class Escape_Sequence {
    public static void main(String[] args){
        System.out.println("MD. Nazmus \bSadat \bNuman");                      //MD. NazmusSadatNuman                      Back space
        System.out.println("Age: \t20");                                       //Age:    20                                Tab
        System.out.print("01645489811\n");                                     //01645489811                               Newline
        System.out.println("email \rEmail: nazmussadatnuman92@gmaol.com");     //Email: nazmussadatnuman92@gmaol.com       Carriage return
        System.out.println("\"Rajbari\"");                                     //"Rajbari"                                 Double quote
        System.out.println("\'Pangsha\'");                                     //'Pangsha'                                 Single quote
        System.out.println("Email\\Phone");                                    //Email\Phone                               Back Slash
    }
}
